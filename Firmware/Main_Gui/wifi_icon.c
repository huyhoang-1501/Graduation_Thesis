
// wifi_icon.c
#include "wifi_icon.h"
#include <stdint.h>

const unsigned char image_wifi_bits[] = {
    0x01,0xf0,0x00,0x06,0x0c,0x00,0x18,0x03,0x00,0x21,0xf0,0x80,
    0x46,0x0c,0x40,0x88,0x02,0x20,0x10,0xe1,0x00,0x23,0x18,0x80,
    0x04,0x04,0x00,0x08,0x42,0x00,0x01,0xb0,0x00,0x02,0x08,0x00,
    0x00,0x40,0x00,0x00,0xa0,0x00,0x00,0x40,0x00,0x00,0x00,0x00
};

// LVGL descriptor
const lv_img_dsc_t wifi_icon = {
    .header.always_zero = 0,
    .header.w = WIFI_ICON_W,
    .header.h = WIFI_ICON_H,
    // 1 bit / pixel -> số byte = (w*h)/8, làm tròn lên
    .data_size = (WIFI_ICON_W * WIFI_ICON_H + 7) / 8,
    .header.cf = LV_IMG_CF_ALPHA_1BIT,
    .data = (const uint8_t *)image_wifi_bits
};